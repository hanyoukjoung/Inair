package kr.or.inair.vo;

public class SalaryVO {
	private String rnum;
	private String salary_num;
	private String salary_scope;
	private String salary_form;
	private String salary_conditon;
	public String getSalary_num() {
		return salary_num;
	}
	public void setSalary_num(String salary_num) {
		this.salary_num = salary_num;
	}
	public String getSalary_scope() {
		return salary_scope;
	}
	public void setSalary_scope(String salary_scope) {
		this.salary_scope = salary_scope;
	}
	public String getSalary_form() {
		return salary_form;
	}
	public void setSalary_form(String salary_form) {
		this.salary_form = salary_form;
	}
	public String getSalary_conditon() {
		return salary_conditon;
	}
	public void setSalary_conditon(String salary_conditon) {
		this.salary_conditon = salary_conditon;
	}
	public String getRnum() {
		return rnum;
	}
	public void setRnum(String rnum) {
		this.rnum = rnum;
	}
	
}
