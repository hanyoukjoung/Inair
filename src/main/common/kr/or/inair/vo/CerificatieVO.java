package kr.or.inair.vo;

public class CerificatieVO {
	private String crqfc_num;
	private String crqfc_name;
	private String crqfc_pblshr;
	private String crqfc_acqdt;
	private String resume_num;
	
	public String getCrqfc_num() {
		return crqfc_num;
	}
	public void setCrqfc_num(String crqfc_num) {
		this.crqfc_num = crqfc_num;
	}
	public String getCrqfc_name() {
		return crqfc_name;
	}
	public void setCrqfc_name(String crqfc_name) {
		this.crqfc_name = crqfc_name;
	}
	public String getCrqfc_pblshr() {
		return crqfc_pblshr;
	}
	public void setCrqfc_pblshr(String crqfc_pblshr) {
		this.crqfc_pblshr = crqfc_pblshr;
	}
	public String getCrqfc_acqdt() {
		return crqfc_acqdt;
	}
	public void setCrqfc_acqdt(String crqfc_acqdt) {
		this.crqfc_acqdt = crqfc_acqdt;
	}
	public String getResume_num() {
		return resume_num;
	}
	public void setResume_num(String resume_num) {
		this.resume_num = resume_num;
	}

	
}
