package kr.or.inair.vo;

public class LowProVO {
	private String lowpro_num;
	private String uppro_num;
	private String lowpro_name;
	private String rnum;
	
	public String getLowpro_num() {
		return lowpro_num;
	}
	public void setLowpro_num(String lowpro_num) {
		this.lowpro_num = lowpro_num;
	}
	public String getUppro_num() {
		return uppro_num;
	}
	public void setUppro_num(String uppro_num) {
		this.uppro_num = uppro_num;
	}
	public String getLowpro_name() {
		return lowpro_name;
	}
	public void setLowpro_name(String lowpro_name) {
		this.lowpro_name = lowpro_name;
	}
	public String getRnum() {
		return rnum;
	}
	public void setRnum(String rnum) {
		this.rnum = rnum;
	}
}	
