package kr.or.inair.vo;

public class UpProVO {
	private String uppro_num;
	private String uppro_name;
	private String rnum;
	
	public String getUppro_num() {
		return uppro_num;
	}
	public void setUppro_num(String uppro_num) {
		this.uppro_num = uppro_num;
	}
	public String getUppro_name() {
		return uppro_name;
	}
	public void setUppro_name(String uppro_name) {
		this.uppro_name = uppro_name;
	}
	public String getRnum() {
		return rnum;
	}
	public void setRnum(String rnum) {
		this.rnum = rnum;
	}
}